//operaçoes matematicas
public class programa15 {

	public static void main(String[] args) {
		int num1 = 5;
		int num2 = 9;
		int res;
		
		//soma
		res = num1 + num2;
		System.out.println("A soma de " + num1 + " + " + num2 + " é " + res );
		
		//subtraçao
		res = num1 - num2;
		System.out.println("A subtracao de " + num1 + " - " + num2 + " é " + res );
		

	}

}
